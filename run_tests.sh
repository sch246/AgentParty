#!/bin/bash
# 测试运行脚本

echo "=========================================="
echo "运行所有测试"
echo "=========================================="

python -m unittest discover tests -v

if [ $? -eq 0 ]; then
    echo ""
    echo "=========================================="
    echo "✅ 所有测试通过！"
    echo "=========================================="
else
    echo ""
    echo "=========================================="
    echo "❌ 测试失败"
    echo "=========================================="
    exit 1
fi
